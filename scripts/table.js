for (let i = 2; i <= 9; i++) {
   // Створюємо заголовок для кожної таблиці
   console.log(`Таблиця множення на ${i}:`);
 
   // Проходимось по числам від 1 до 10 та множимо наше число на кожне з них
   for (let j = 1; j <= 10; j++) {
     // Виводимо результат множення
     console.log(`${i} x ${j} = ${i * j}`);
   }
 }